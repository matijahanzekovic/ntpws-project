            <hr><h1>Cleo Smith: 'Grave concerns' for Australian girl missing from beach campsite</h1>
            <h2>Australian police say they have "grave concerns" for a four-year-old girl who disappeared from a remote coastal campsite at the weekend.</h2>
            <p>Cleo Smith was last seen sleeping in her family's tent at the Quobba Blowholes camping ground in Western Australia early on Saturday morning.</p>
            <p>Her mother said early the next morning the tent was open, and the girl was gone, along with her sleeping bag.</p>
            <p>Cleo's mother, Ellie Smith, told local media of their "horrendous" past few days. "We haven't really slept," she said at an emotional news conference.</p>
            <p>"Everyone asks us what what we need and all we need is our little girl home... The worst part is, we can't do anything more. It's out of our hands so we feel hopeless and out of control."</p>
            <p>Authorities have not disclosed any information on what they think happened to the girl, but have said they will "leave no stone unturned".</p>
            <p>On Tuesday morning, regional Inspector Jon Munday told breakfast TV show Sunrise that investigators could not rule out the possibility that she might have been "taken and removed from the area".</p>
